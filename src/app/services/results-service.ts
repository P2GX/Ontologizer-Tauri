import { computed, Injectable, signal } from '@angular/core';
import { invoke } from '@tauri-apps/api/core';
import { Method } from './analysis-service';

@Injectable({
  providedIn: 'root'
})
export class ResultsService {
  public currentMethod = signal<Method | null>(null);
  public isBayesian = computed(() => this.currentMethod()?.method === 'bayesian');
  public bayesianTableData = signal<BayesianRowData[] | null>(null);

  clearResults() {
    this.frequentistTableData = null;
    this.bayesianTableData.set(null);
    this.dotData = null;
    this.significantCount = 0;
    this.resultsLength = 0;
    this.proportionData = {
      total: { nonSignificant: 0, significant: 0 },
      BP: { nonSignificant: 0, significant: 0 },
      MF: { nonSignificant: 0, significant: 0 },
      CC: { nonSignificant: 0, significant: 0 }
    };
  }

  async runAnalysis() {
    try {
      // Call the Tauri command to start the analysis
      await invoke<void>('run_analysis');
    } catch (error) {
      console.error('Error running analysis:', error);
      this.frequentistTableData = null;
      this.dotData = null;
      throw error;
    }
  }

  private frequentistTableData: FrequentistRowData[] | null = null;
  
  private dotData: DotData | null = null;
  private mtcMethod: string = '';
  
  private significantCount: number = 0;
  private resultsLength: number = 0;
  private proportionData: ProportionData = {
    total: { nonSignificant: 0, significant: 0 },
    BP: { nonSignificant: 0, significant: 0 },
    MF: { nonSignificant: 0, significant: 0 },
    CC: { nonSignificant: 0, significant: 0 }
  };

  async loadDotData() {
    try {
      const data = await invoke<DotData>('build_go_graph_data');
      this.dotData = { BP: data.BP, MF: data.MF, CC: data.CC };
      console.log('Loaded dot data:', this.dotData);
    } catch (error) {
      console.error('Error loading dot data:', error);
    }
  }

  async loadAnalysisOutput() {
    try {
      const data = await invoke<string>('get_analysis_results');
      const parsedData = JSON.parse(data);

      const items = parsedData.items || [];
      this.resultsLength = items.length;

      if (this.currentMethod()?.method === 'bayesian') {
        this.bayesianTableData.set(this.parseBayesianResults(items));
      } else {
        this.frequentistTableData = this.parseAnalysisResults(items);
        this.significantCount = items.filter((item: any) => item.Score <= 0.05).length;
        this.calculateProportions();
      }
    } catch (error) {
      console.error('Error loading analysis output:', error);
      this.frequentistTableData = null;
      this.bayesianTableData.set(null);
      throw error;
    }
  }

  parseAnalysisResults(items: any[]): FrequentistRowData[] {
    return items.map(item => {
      let k = 0, n = 0, K = 0, N = 0;

      // Parse the diagnostics string generated by Rust format!("{:?}", (nt, mt, n, m))
      if (item.Diagnostics) {
        const counts = item.Diagnostics.replace(/[()]/g, '').split(',');
        if (counts.length === 4) {
          k = parseInt(counts[0].trim(), 10) || 0;
          n = parseInt(counts[1].trim(), 10) || 0;
          K = parseInt(counts[2].trim(), 10) || 0;
          N = parseInt(counts[3].trim(), 10) || 0;
        }
      }

      return {
        id: item.Id,
        label: item.Label,
        aspect: item.Aspect,
        p_val: item.Score,
        associatedGenes: item['Associated Genes'] ? item['Associated Genes'].split(', ') : [],
        diagnostics: item.Diagnostics,
        k,
        n,
        K,
        N
      };
    });
  }

  parseBayesianResults(items: any[]): BayesianRowData[] {
    return items.map(item => ({
      id: item.Id,
      label: item.Label,
      aspect: item.Aspect,
      score: item.Score,
      associatedGenes: item['Associated Genes'] ? item['Associated Genes'].split(', ') : [],
    }));
  }

  calculateProportions() {
    this.proportionData = {
      total: { nonSignificant: 0, significant: 0 },
      BP: { nonSignificant: 0, significant: 0 },
      MF: { nonSignificant: 0, significant: 0 },
      CC: { nonSignificant: 0, significant: 0 }
    };

    if (!this.frequentistTableData) return;

    this.frequentistTableData.forEach((row: FrequentistRowData) => {
      const aspect = row.aspect as keyof ProportionData;
      const index = row.p_val <= 0.05 ? 'significant' : 'nonSignificant';

      // Ensure the aspect exists in our proportionData keys ('BP', 'MF', 'CC')
      if (aspect && this.proportionData[aspect]) {
        this.proportionData[aspect][index] += 1;
      }

      this.proportionData.total[index] += 1;
    });

    console.log('Proportion Data:', this.proportionData);
  }

  get hasResults(): boolean {
    return this.frequentistTableData !== null || this.bayesianTableData() !== null;
  }

  // Getters for components to access the data
  getMethod() { return this.currentMethod(); }
  getFrequentistTableData() { return this.frequentistTableData; }
  getBayesianTableData() { return this.bayesianTableData; }
  getDotData() { return this.dotData; }
  getMtcMethod() { return this.mtcMethod; }
  getSignificantCount() { return this.significantCount; }
  getResultsLength() { return this.resultsLength; }
  getProportionData() { return this.proportionData; }
}

export interface ProportionData {
  total: { nonSignificant: number, significant: number };
  BP: { nonSignificant: number, significant: number };
  MF: { nonSignificant: number, significant: number };
  CC: { nonSignificant: number, significant: number };
}

// Graph interfaces: 1 graph/tree for each GO-Aspect
export interface DotData {
  BP: { nodes: Nodes, edges: Edges };
  MF: { nodes: Nodes, edges: Edges };
  CC: { nodes: Nodes, edges: Edges };
}

export interface Nodes {
  significant: { [termId: string]: NodeData };
  ancestors: { [termId: string]: NodeData };
}

export interface NodeData {
  id: string,    // GO-Term-ID
  label: string, // Term-Label
  p_val: number,
  study_count: number,
  population_count: number,
  depth: number,            // depth in the graph
}

export interface Edges {
  compressed: EdgeData[];
  full: EdgeData[];
}

export interface EdgeData {
  source: string, // significant parent node
  target: string, // significant child node
  nodes_skipped: number, // counts how many non-significant nodes were skipped between source and target
  // 0 (solid edge) if direct parent is significant, otherwise >0 (dashed edge)
}

export interface BayesianRowData {
  id: string;
  label: string;
  aspect: string;
  score: number;
  associatedGenes: string[];
}

export interface FrequentistRowData {
  id: string;
  label: string;
  aspect: string;
  p_val: number;
  associatedGenes: string[];
  diagnostics?: string;
  k: number;
  n: number;
  K: number;
  N: number;
}

